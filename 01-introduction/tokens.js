// Below is the document that covers JavaScript tokens with only the essential content.

// ------------------------------------------------------------
//                          JavaScript Tokens

// 1. Keywords
// -------------
// Reserved words predefined by JavaScript (ES6+):

// await, break, case, catch, class, const, continue, debugger, default, delete, do, else, export, extends, false, finally, for, function, if, import, in, instanceof, new, null, return, super, switch, this, throw, true, try, typeof, var, void, while, with, yield
// let, static (Future reserved words in strict mode or generally)
// enum (Reserved for future use)
// implements, interface, package, private, protected, public (Strict mode reserved)

// 2. Identifiers
// ----------------
// Custom names for variables, functions, classes, labels, properties, etc.

// Rules:
//   - Must start with a letter, underscore (_), or dollar sign ($).
//   - Must not be a reserved keyword.
//   - Digits (0-9) are allowed after the first character.
//   - Case-sensitive.

// Example:
//   let age;
//   const firstName = "John";
//   let $result;
//   let _privateVar;

// 3. Literals
// ------------
// Fixed values (constant values) used in the script.

// Types:
//   ● Integer literals: 123, 0xFF (Hex), 0o77 (Octal), 0b1010 (Binary)
//   ● BigInt literal: 9007199254740991n
//   ● Floating-point literals: 3.14, 2e10, .5
//   ● String literals: "Hello", 'World'
//   ● Template literals: `Hello ${name}` (Backticks)
//   ● Boolean literals: true, false
//   ● Null literal: null
//   ● RegExp literals: /ab+c/g

// 4. Operators
// ------------
// Symbols or keywords that perform operations.

// ➤ Arithmetic Operators:
//     +, -, *, /, %, ** (Exponentiation)

// ➤ Assignment Operators:
//     =, +=, -=, *=, /=, %=, **=
//     <<=, >>=, >>>=, &=, |=, ^=
//     &&=, ||=, ??= (Logical Assignment)

// ➤ Comparison Operators:
//     ==, != (Loose equality)
//     ===, !== (Strict equality)
//     >, <, >=, <=

// ➤ Logical Operators:
//     && (AND), || (OR), ! (NOT)
//     ?? (Nullish Coalescing)

// ➤ Bitwise Operators:
//     &, |, ^, ~, <<, >>, >>> (Zero-fill right shift)

// ➤ Unary Operators:
//     +, -, ++, --, !, typeof, delete, void, ~

// ➤ Ternary Operator:
//     ? :

// ➤ Relational Operators:
//     instanceof, in

// 5. Punctuators / Separators
// ---------------------------
// Used to group or separate code elements.

//   • Parentheses: ( )
//   • Braces: { }
//   • Brackets: [ ]
//   • Semicolon: ;
//   • Comma: ,
//   • Dot: .
//   • Spread/Rest: ...
//   • Arrow: =>
//   • Optional Chaining: ?.
//   • Colon: :

